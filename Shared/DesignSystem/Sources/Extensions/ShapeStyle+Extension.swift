import SwiftUI

public extension ShapeStyle where Self == Color {
    static var musyMain: Color {
        return .musyMain
    }
    
    static var musySub: Color {
        return .musySub
    }
    
    static var musyPink: Color {
        return .musyPink
    }
    
    static var musyRed: Color {
        return .musyRed
    }
    
    static var musyYellow: Color {
        return .musyYellow
    }
    
    static var musyGreen: Color {
        return .musyGreen
    }
}
